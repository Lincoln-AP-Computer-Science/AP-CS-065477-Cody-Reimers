/* The letters in this program are hypothetical letters that I might send after I'm at college,
using hypothetical scenarios that, while not true now, and may not ever be true, could
possibly happen. */
public class Letters1_2
{
   
   public static void main(String[] args)
   {
      stu();
      jerry();
      jayme();
   }  // end of main
   
   public static void stu()
   {
      System.out.println("Dear Stuart,");
      System.out.println("   Hey, it's me, Cody! It's good to correspond\nwith you, since it's been so long.");
      classes();
      goals();
      freeTime();
      System.out.println("Well, it was good to get to talk to you a little.");
      sincerely();
   }  // end of stu
   
   public static void classes()
   {
      System.out.println("School's been a lot of work this year!");
      System.out.println("I'm part of a programming course, which has");
      System.out.println("been fun and exhausting. I'm also part of a");
      System.out.println("choir, of course. We're currently practicing");
      System.out.println("for a big consert. You should come if you can!");
   }  // end of classes
   
   public static void goals()
   {
      System.out.println("I've put in a lot of work towards making a big");
      System.out.println("program. It's going to be really cool when it's");
      System.out.println("done! I'm hoping that, in a few years, once my");
      System.out.println("studies are complete, I'll be able to get");
      System.out.println("hired by a big company as a coder or designer.");
   }  // end of goals
   
   public static void freeTime()
   {
      System.out.println("I still love playing video games, like always,");
      System.out.println("but now I've found a club for League of");
      System.out.println("Legends where we train together to become");
      System.out.println("better players. I've also read a couple");
      System.out.println("of good books recently, I recommend you");
      System.out.println("check out Apocalypse Troll. Great sci-fi book.");
   }  // end of freeTime
   
   public static void sincerely()
   {
      System.out.println("Sincerely, Cody");
   }  // end of sincerely
   
   public static void jerry()
   {
      System.out.println("Dear Jerry,");
      System.out.println("   Wanted to send you a letter and let you\nknow what life's been like for me at college.");
      freeTime();
      classes();
      System.out.println("I hope life's been treating you well.");
      System.out.println("I love you");
      sincerely();
   }  // end of jerry
   
   public static void jayme()
   {
      System.out.println("Dear Jayme,");
      System.out.println("   It's been a while since we've talked,\n or done anything together, hasn't it?");
      System.out.println("I really want to try and reconnect with");
      System.out.println("at some point. I remember the old days.");
      freeTime();
      System.out.println("I want to talk to you more some time,");
      System.out.println("So feel free to send a letter whenever.");
      sincerely();
   }  // end of jayme
   
}  // end of class Letters